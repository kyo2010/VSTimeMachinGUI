  private void jButOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButOkActionPerformed
    try{
      PORT_LISTING_INT = Integer.parseInt(PORT_FOR_LISTING.getText());
    }catch(Exception e){}
    try{
      PORT_SENDING_INT = Integer.parseInt(PORT_FOR_SENDING.getText());
    }catch(Exception e){}
    
    VS_SETTING.setParam(mainForm.con, "PORT_LISTING_INT", ""+singelton.PORT_LISTING_INT);
    VS_SETTING.setParam(mainForm.con, "PORT_SENDING_INT", ""+singelton.PORT_SENDING_INT);
    VS_SETTING.setParam(mainForm.con, "WAN_CONNECTION",""+jcNetworkInterface.getSelectedItem());
    
    if (mainForm.vsTimeConnector!=null){
      mainForm.disconnect();
      mainForm.connect();
    }
    
    setVisible(false);
  }//GEN-LAST:event_jButOkActionPerformed

